    public void slideShow() {
        new Thread(new Runnable() {
            @Override
            public void run() {
                int nb = 0;
                try {
                    while (true) {
                        switch (nb) {
                            case 0:
                                Thread.sleep(1000);
                                sc.jLabelXLeft(0, -500, 12, 10, img1);
                                sc.jLabelXLeft(500, 0, 12, 10, img2);
                                sc.jLabelXLeft(1000, 500, 12, 10, img3);
                                nb++;
                            case 1:
                                Thread.sleep(1000);
                                sc.jLabelXLeft(-500, -1000, 12, 10, img1);
                                sc.jLabelXLeft(0, -500, 12, 10, img2);
                                sc.jLabelXLeft(500, 0, 12, 10, img3);
                                nb++;
                            case 2:
                                Thread.sleep(1000);
                                sc.jLabelXLeft(-1000, -500, 12, 10, img1);
                                sc.jLabelXLeft(-500, 0, 12, 10, img2);
                                sc.jLabelXLeft(500, 0, 12, 10, img3);
                                nb++;
                            case 3:
                                Thread.sleep(1000);
                                sc.jLabelXLeft(-500, 0, 12, 10, img1);
                                sc.jLabelXLeft(0, 500, 12, 10, img2);
                                sc.jLabelXLeft(500, 1000, 12, 10, img3);
                                nb = 0;
                        }
                    }
                } catch (Exception e) {
                }
            }
        }).start();
    }

